def addNumbers(num1, num2):
    print(f"{num1} + {num2} = {num1 + num2}")
    
def subtractNumbers(num1, num2):
    print(f"{num1} - {num2} = {num1 - num2}")
    
def multiplyNumbers(num1, num2):
    print(f"{num1} * {num2} = {num1 * num2}")
    
def divideNumbers(num1, num2):
    print(f"{num1} / {num2} = {num1 / num2}")
    if num2 == 0:
        print("Cannot divide by zero")